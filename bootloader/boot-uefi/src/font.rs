/// Draw a single character at pixel position (x, y) using the bitmap font.
/// — NeonVale: one glyph at a time, painting the pre-boot void with meaning
pub fn draw_char(
    gop: *mut EfiGraphicsOutputProtocol,
    x: usize,
    y: usize,
    ch: u8,
    fg: EfiBltPixel,
    bg: EfiBltPixel,
) {
    let glyph = if (ch as usize) < 128 {
        &FONT_DATA[ch as usize]
    } else {
        &FONT_DATA[b'?' as usize]
    };

    for row in 0..FONT_HEIGHT {
        let bits = glyph[row];
        for col in 0..FONT_WIDTH {
            let pixel = if bits & (0x80 >> col) != 0 { fg } else { bg };
            crate::blt_fill(gop, pixel, x + col, y + row, 1, 1);
        }
    }
}

/// Draw a string at pixel position (x, y).
/// Returns the x position after the last character (for chaining).
/// — NeonVale: the font renderer's workhorse — string goes in, pixels come out
pub fn draw_string(
    gop: *mut EfiGraphicsOutputProtocol,
    x: usize,
    y: usize,
    text: &str,
    fg: EfiBltPixel,
    bg: EfiBltPixel,
) -> usize {
    let mut cx = x;
    for &byte in text.as_bytes() {
        if cx + FONT_WIDTH > 4096 {
            break; // — NeonVale: don't draw off the edge of reality
        }
        draw_char(gop, cx, y, byte, fg, bg);
        cx += FONT_WIDTH;
    }
    cx
}

/// Draw a string from a fixed-size byte buffer (null-terminated or full).
/// — NeonVale: for our heap-free boot config strings
pub fn draw_buf_string(
    gop: *mut EfiGraphicsOutputProtocol,
    x: usize,
    y: usize,
    buf: &[u8],
    fg: EfiBltPixel,
    bg: EfiBltPixel,
) -> usize {
    let mut cx = x;
    for &byte in buf {
        if byte == 0 {
            break;
        }
        if cx + FONT_WIDTH > 4096 {
            break;
        }
        draw_char(gop, cx, y, byte, fg, bg);
        cx += FONT_WIDTH;
    }
    cx
}

/// Fill a rectangular region with a solid color.
/// — NeonVale: sometimes you just need to paint over the evidence
pub fn fill_rect(
    gop: *mut EfiGraphicsOutputProtocol,
    x: usize,
    y: usize,
    width: usize,
    height: usize,
    color: EfiBltPixel,
) {
    // Use wider fills for better performance — one BLT per row instead of per-pixel
    for row in 0..height {
        crate::blt_fill(gop, color, x, y + row, width, 1);
    }
}

/// Draw a horizontal line.
pub fn draw_hline(
    gop: *mut EfiGraphicsOutputProtocol,
    x: usize,
    y: usize,
    width: usize,
    color: EfiBltPixel,
) {
    crate::blt_fill(gop, color, x, y, width, 1);
}

/// Format a u32 as decimal into a buffer. Returns the number of bytes written.
/// — NeonVale: itoa without the crate — because pulling in dependencies for a number is peak 2026
pub fn format_u32(buf: &mut [u8], mut val: u32) -> usize {
    if val == 0 {
        if !buf.is_empty() {
            buf[0] = b'0';
        }
        return 1;
    }
    let mut tmp = [0u8; 10];
    let mut len = 0;
    while val > 0 {
        tmp[len] = b'0' + (val % 10) as u8;
        val /= 10;
        len += 1;
    }
    for i in 0..len.min(buf.len()) {
        buf[i] = tmp[len - 1 - i];
    }
    len.min(buf.len())
}
