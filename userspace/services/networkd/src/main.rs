//! OXIDE Network Daemon (networkd)
//!
//! Multi-adapter network manager service that handles:
//! - Discovery and registration of network adapters
//! - Static IP configuration from /etc/network/<iface>.conf
//! - DHCP client for dynamic IP assignment
//! - DNS server configuration in /etc/resolv.conf
//! - Adapter lifecycle management (up/down/reload)
//! - Real-time adapter state monitoring
//!
//! Config file format (/etc/network/eth0.conf):
//! ```text
//! mode=static|dhcp|manual
//! address=192.168.1.100
//! netmask=255.255.255.0
//! gateway=192.168.1.1
//! dns=8.8.8.8
//! dns=8.8.4.4
//! mtu=1500
//! ```
//!
//! Persona: ShadePacket - Networking stack engineer

